#include "func4.h"

// Алгоритм Евклида
int GCF(int A, int B)
{
    // Сохраняем оригинальные значения для безопасности
    int a = A;
    int b = B;
    
    // Защита от нулевых значений
    if (a == 0 && b == 0) {
        return 0; // НОД(0,0) не определён, возвращаем 0
    }
    if (a == 0) {
        return b; // НОД(0,B) = B
    }
    if (b == 0) {
        return a; // НОД(A,0) = A
    }
    
    // Основной цикл алгоритма Евклида
    while (b != 0) {
        int remainder = a % b; // остаток от деления
        a = b;
        b = remainder;
    }
    
    return (a > 0) ? a : -a; // возвращаем абсолютное значение
}
