#include <stdio.h>
#include "func4.h"

int main()
{
    int num1, num2;
    
    printf("Программа для вычисления НОД двух чисел\n");
    printf("=========================================\n\n");
    
    // Ввод первого числа
    printf("Введите первое натуральное число: ");
    if (scanf("%d", &num1) != 1) {
        printf("Ошибка ввода! Пожалуйста, введите целое число.\n");
        return 1;
    }
    
    // Ввод второго числа
    printf("Введите второе натуральное число: ");
    if (scanf("%d", &num2) != 1) {
        printf("Ошибка ввода! Пожалуйста, введите целое число.\n");
        return 1;
    }
    
    // Проверка на натуральные числа
    if (num1 <= 0 || num2 <= 0) {
        printf("Ошибка: числа должны быть натуральными (больше 0).\n");
        printf("Вы ввели: %d и %d\n", num1, num2);
        return 1;
    }
    
    // Вычисление НОД
    int gcd = GCF(num1, num2);
    
    // Вывод результата
    printf("\nРезультат:\n");
    printf("НОД(%d, %d) = %d\n", num1, num2, gcd);
    
    // Дополнительная информация
    printf("\nПроверка:\n");
    printf("%d / %d = %d (остаток %d)\n", 
           num1, gcd, num1 / gcd, num1 % gcd);
    printf("%d / %d = %d (остаток %d)\n", 
           num2, gcd, num2 / gcd, num2 % gcd);
    
    if (num1 % gcd == 0 && num2 % gcd == 0) {
        printf("✓ Оба числа делятся на НОД без остатка\n");
    }
    
    return 0;
}